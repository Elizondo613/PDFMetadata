    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        // TODO add your handling code here:
        
        JFileChooser selectorArchivo = new JFileChooser();
        selectorArchivo.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        selectorArchivo.setFileFilter(filtro);
        int resultado = selectorArchivo.showOpenDialog(this);
        
        if ( resultado == JFileChooser.APPROVE_OPTION ){
            //SELECCIÓN DE METADATOS
            File archivo = new File(selectorArchivo.getSelectedFile().getName());
            String nombre = selectorArchivo.getSelectedFile().getName();
            long modificacion = selectorArchivo.getSelectedFile().lastModified();
            long tamanio = selectorArchivo.getSelectedFile().getTotalSpace();
            boolean edit = archivo.canWrite();
            boolean read = archivo.canRead();
            
            //MUESTRA LOS METADATOS
            Nombre.setText(nombre);
            Tamanio.setText(Long.toString(tamanio));
            Modificacion.setText(Long.toString(modificacion));
            Read.setText(Boolean.toString(read));
            Edit.setText(Boolean.toString(edit));
        }
        
    }//GEN-LAST:event_BuscarActionPerformed

