    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        // TODO add your handling code here:
        //BUSCA UN ARCHIVO PARA ELIMINARLO
        JFileChooser selectorArchivo = new JFileChooser();
        selectorArchivo.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        selectorArchivo.setFileFilter(filtro);
        int resultado = selectorArchivo.showOpenDialog(this);
        
        if ( resultado == JFileChooser.APPROVE_OPTION ){  
            File myObj = new File(selectorArchivo.getSelectedFile().getAbsolutePath()); 
            if (myObj.delete()) {
              JOptionPane.showMessageDialog(null, "Archivo eliminado: " + myObj.getName());
            } else {
              JOptionPane.showMessageDialog(null, "Error al intentar eliminar!");
            }  
        }
    }//GEN-LAST:event_EliminarActionPerformed

    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed
        // TODO add your handling code here:
        //BUSCA UN ARCHIVO PARA EDITARLO
        JFileChooser selectorArchivo = new JFileChooser();
        selectorArchivo.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        selectorArchivo.setFileFilter(filtro);
        int resultado = selectorArchivo.showOpenDialog(this);
        
        if ( resultado == JFileChooser.APPROVE_OPTION ){  
            String name = JOptionPane.showInputDialog("Ingresa nuevo nombre");
            File oldFile = new File(selectorArchivo.getSelectedFile().getName());
            
            File newFile = new File(selectorArchivo.getSelectedFile().getName() + name + ".pdf");
            
            if(oldFile.renameTo(newFile)){
                JOptionPane.showMessageDialog(null, "Nombre modificado");
            } else{
                JOptionPane.showMessageDialog(null, "Error");
            }
        }
    }//GEN-LAST:event_EditarActionPerformed

